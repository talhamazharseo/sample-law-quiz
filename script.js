function renderQuestion() {
  const question = questions[currentQuestion];

  selectedAnswer = responses[currentQuestion];

  counter.textContent = `Question ${currentQuestion + 1} of ${questions.length}`;
  categoryEnglish.textContent = question.categoryEnglish;
  categoryUrdu.textContent = question.categoryUrdu;
  questionEnglish.textContent = question.questionEnglish;
  questionUrdu.textContent = question.questionUrdu;

  const completedForProgress = currentQuestion + 1;
  const progressPercent = (completedForProgress / questions.length) * 100;

  progressBar.style.width = `${progressPercent}%`;
  progressTrack.setAttribute("aria-valuenow", String(completedForProgress));

  optionsElement.innerHTML = "";
  lockedMessage.classList.toggle("show", selectedAnswer !== null);

  question.options.forEach((option, index) => {
    const button = document.createElement("button");

    button.type = "button";
    button.className = "option";
    button.disabled = selectedAnswer !== null;
    button.setAttribute(
      "aria-label",
      `${String.fromCharCode(65 + index)}. ${option.en}`
    );

    if (selectedAnswer === index) {
      button.classList.add("selected");
      button.setAttribute("aria-pressed", "true");
    } else {
      button.setAttribute("aria-pressed", "false");
    }

    button.innerHTML = `
      <span class="option-letter">${String.fromCharCode(65 + index)}</span>
      <span class="option-text">
        <span class="option-english">${option.en}</span>
        <span class="urdu option-urdu" lang="ur" dir="rtl">${option.ur}</span>
      </span>
    `;

    button.addEventListener("click", () => lockAnswer(index));
    optionsElement.appendChild(button);
  });

  nextButton.disabled = selectedAnswer === null;
  nextButton.textContent =
    currentQuestion === questions.length - 1
      ? "Finish Quiz · کوئز مکمل کریں"
      : "Next Question · اگلا سوال";
}

function lockAnswer(index) {
  if (responses[currentQuestion] !== null) {
    return;
  }

  responses[currentQuestion] = index;
  selectedAnswer = index;
  renderQuestion();
}

function goToNextQuestion() {
  if (responses[currentQuestion] === null) {
    return;
  }

  if (currentQuestion < questions.length - 1) {
    currentQuestion += 1;
    renderQuestion();
    return;
  }

  showResults();
}

function calculateScore() {
  return responses.reduce((total, response, index) => {
    return total + (response === questions[index].answer ? 1 : 0);
  }, 0);
}

function showResults() {
  const score = calculateScore();
  const percentage = Math.round((score / questions.length) * 100);

  quizArea.style.display = "none";
  results.classList.add("show");

  scoreValue.textContent = `${score}/${questions.length}`;
  scoreRing.style.background = `
    conic-gradient(
      var(--primary) ${percentage * 3.6}deg,
      #e5ece8 0deg
    )
  `;

  if (percentage >= 85) {
    resultText.textContent =
      `Excellent. You scored ${score} out of ${questions.length} (${percentage}%).`;
    resultTextUrdu.textContent =
      `بہترین۔ آپ نے ${questions.length} میں سے ${score} نمبر حاصل کیے (${percentage} فیصد)۔`;
  } else if (percentage >= 65) {
    resultText.textContent =
      `Good work. You scored ${score} out of ${questions.length} (${percentage}%).`;
    resultTextUrdu.textContent =
      `اچھی کوشش۔ آپ نے ${questions.length} میں سے ${score} نمبر حاصل کیے (${percentage} فیصد)۔`;
  } else {
    resultText.textContent =
      `You scored ${score} out of ${questions.length} (${percentage}%). Review the answers and try again.`;
    resultTextUrdu.textContent =
      `آپ نے ${questions.length} میں سے ${score} نمبر حاصل کیے (${percentage} فیصد)۔ جوابات کا جائزہ لے کر دوبارہ کوشش کریں۔`;
  }

  renderAnswerReview();

  window.scrollTo({
    top: 0,
    behavior: "smooth"
  });
}

function renderAnswerReview() {
  reviewList.innerHTML = "";

  questions.forEach((question, index) => {
    const userAnswerIndex = responses[index];
    const correctAnswerIndex = question.answer;
    const isCorrect = userAnswerIndex === correctAnswerIndex;
    const userAnswer = question.options[userAnswerIndex];
    const correctAnswer = question.options[correctAnswerIndex];

    const item = document.createElement("article");
    item.className = `review-item ${isCorrect ? "correct" : "incorrect"}`;

    item.innerHTML = `
      <div class="review-number">
        Question ${index + 1} · ${isCorrect ? "Correct" : "Incorrect"}
      </div>

      <p class="review-question">${question.questionEnglish}</p>
      <p class="urdu review-question-urdu" lang="ur" dir="rtl">
        ${question.questionUrdu}
      </p>

      <p class="review-answer">
        <span class="label">Your answer:</span> ${userAnswer.en}
        <span class="urdu" lang="ur" dir="rtl">
          آپ کا جواب: ${userAnswer.ur}
        </span>
      </p>

      <p class="review-answer">
        <span class="label">Correct answer:</span> ${correctAnswer.en}
        <span class="urdu" lang="ur" dir="rtl">
          درست جواب: ${correctAnswer.ur}
        </span>
      </p>

      <p class="review-explanation">
        ${question.explanationEnglish}
        <span class="urdu" lang="ur" dir="rtl">
          ${question.explanationUrdu}
        </span>
      </p>
    `;

    reviewList.appendChild(item);
  });
}

function restartQuiz() {
  currentQuestion = 0;
  selectedAnswer = null;
  responses = Array(questions.length).fill(null);

  results.classList.remove("show");
  quizArea.style.display = "block";
  reviewList.innerHTML = "";

  renderQuestion();

  window.scrollTo({
    top: 0,
    behavior: "smooth"
  });
}

function showToast(message) {
  toast.textContent = message;
  toast.classList.add("show");

  window.setTimeout(() => {
    toast.classList.remove("show");
  }, 1800);
}

async function shareResult() {
  const score = calculateScore();
  const percentage = Math.round((score / questions.length) * 100);
  const text =
    `I scored ${score}/${questions.length} (${percentage}%) on the ` +
    "Pakistan Criminal & Civil Law Quiz.";

  const shareData = {
    title: "Pakistan Criminal & Civil Law Quiz",
    text,
    url: window.location.href
  };

  try {
    if (navigator.share) {
      await navigator.share(shareData);
      return;
    }

    if (navigator.clipboard) {
      await navigator.clipboard.writeText(`${text} ${window.location.href}`);
      showToast("Result and link copied!");
      return;
    }

    showToast("Sharing is not supported in this browser.");
  } catch (error) {
    if (error.name !== "AbortError") {
      showToast("Could not share the result.");
    }
  }
}

